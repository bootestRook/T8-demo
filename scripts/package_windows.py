#!/usr/bin/env python3
"""Build a native Windows playable package from the Godot project."""
from __future__ import annotations

import argparse
import json
import shutil
import subprocess
import sys
import zipfile
from pathlib import Path
from typing import Any

if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8", errors="replace")
if hasattr(sys.stderr, "reconfigure"):
    sys.stderr.reconfigure(encoding="utf-8", errors="replace")

PROJECT_ROOT = Path(__file__).resolve().parent.parent
EXPORTS_DIR = PROJECT_ROOT / "exports"
DEFAULT_GAME_NAME = "T8-demo"
DEFAULT_OUT_DIR = EXPORTS_DIR / "T8-demo-windows"
DEFAULT_ZIP = EXPORTS_DIR / "T8-demo-windows.zip"
TEMPLATE_ENTRY = "templates/windows_release_x86_64.exe"
EXPORT_PRESET = "Windows Desktop"
WINDOWS_TEMPLATE = PROJECT_ROOT / ".runtime" / "export_templates" / "windows_release_x86_64.exe"
GODOT_IGNORE_DIRS = ("exports", "reports", ".runtime", "tools", "html5")

sys.path.insert(0, str(PROJECT_ROOT / "scripts"))
from godot_locator import find_godot  # noqa: E402


